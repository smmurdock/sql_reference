-- REMOVING DATA FROM ALL ROWS IN A TABLE

-- To delete all rows from a table:
-- Structure:
-- DELETE FROM <table>;

-- Examples:
DELETE FROM logs;
DELETE FROM users;
DELETE FROM products;

-- The library has closed down, the books have been sold, and we need to remove our user data
DELETE FROM books;
DELETE FROM patrons;

-- YOU CANNOT UNDO THIS. WITHOUT A BACKUP OF YOUR DATA, YOU CANNOT RESTORE THE INFORMATION